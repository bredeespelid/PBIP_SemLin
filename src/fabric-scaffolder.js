/**
 * Fabric Scaffolder
 * Generates a complete Rayfin Fabric Data App seed from the in-memory parsed model.
 * Returns a flat map of relative file paths to file content strings — no DOM interaction,
 * no re-parsing. The caller (app.js → scaffoldRayfin()) writes them to disk.
 *
 * Output structure:
 *   package.json
 *   fabric.yaml
 *   tsconfig.json
 *   AGENTS.md
 *   README.md
 *   visuals/{MeasureName}.dax
 *   visuals/{MeasureName}.json
 *   visuals/{MeasureName}.ts
 *   index.ts  (barrel export)
 */

const FabricScaffolder = {

    /**
     * Build all scaffold files from the already-parsed in-memory model.
     * @param {Object} model        parsedModel from TMDLParser
     * @param {Object} visualData   VisualParser output (may be null)
     * @param {Object} lineageEngine LineageEngine (may be null)
     * @returns {Object}  { relativePath: content, ... }
     */
    scaffold(model, visualData, lineageEngine) {
        const modelName = model.database?.name || model.model?.name || 'SemanticModel';
        const topMeasures = this._topMeasures(model, visualData, lineageEngine);
        const files = {};

        files['package.json']  = this._packageJson(modelName);
        files['fabric.yaml']   = this._fabricYaml(modelName);
        files['tsconfig.json'] = this._tsconfig();
        files['AGENTS.md']     = this._agentsMd(modelName, topMeasures);
        files['README.md']     = this._readmeMd(modelName, topMeasures);

        const exports = [];
        for (const m of topMeasures) {
            const safe = this._safeName(m.name);
            files[`visuals/${safe}.dax`]  = this._daxQuery(m);
            files[`visuals/${safe}.json`] = this._d3Spec(m);
            files[`visuals/${safe}.ts`]   = this._typescript(m, modelName);
            exports.push(safe);
        }

        files['index.ts'] = this._barrelExport(exports);

        return files;
    },

    // ─────────────────────────────────────────────────────────
    // Top measures selector
    // ─────────────────────────────────────────────────────────

    _topMeasures(model, visualData, lineageEngine) {
        // Prefer measures with the most visual appearances
        if (lineageEngine && visualData) {
            const top = lineageEngine.getTopMeasuresByVisualCount(5);
            if (top.length > 0) return top;
        }
        // Fallback: first 5 measures across all visible tables
        const result = [];
        for (const table of model.tables) {
            if (table.isHidden || table._isAutoDate) continue;
            for (const m of table.measures) {
                result.push({ name: m.name, table: table.name, visualCount: 0, pageCount: 0, expression: m.expression || '' });
                if (result.length >= 5) return result;
            }
        }
        return result;
    },

    // ─────────────────────────────────────────────────────────
    // File generators
    // ─────────────────────────────────────────────────────────

    _packageJson(modelName) {
        const pkg = {
            name: this._kebab(modelName),
            version: '0.1.0',
            private: true,
            scripts: {
                dev:   'rayfin dev',
                build: 'rayfin build',
                deploy: 'bunx rayfin up'
            },
            dependencies: {
                '@microsoft/rayfin': 'latest'
            },
            devDependencies: {
                typescript: '^5.0.0'
            }
        };
        return JSON.stringify(pkg, null, 2);
    },

    _fabricYaml(modelName) {
        return [
            `# Fabric Data App — ${modelName}`,
            `# Generated by PBIP SemLin — update workspaceName and datasetId before bun run dev`,
            ``,
            `app:`,
            `  name: "${modelName}"`,
            `  version: 0.1.0`,
            ``,
            `semantic_model:`,
            `  workspace: "<YOUR_WORKSPACE_NAME>"`,
            `  dataset:   "<YOUR_DATASET_ID>"`,
            `  # Find these in Fabric portal: Settings → Semantic model → Connection details`,
            ``,
            `dev:`,
            `  port: 5173`,
            `  open: true`,
            ``
        ].join('\n');
    },

    _tsconfig() {
        return JSON.stringify({
            compilerOptions: {
                target: 'ES2020',
                module: 'ESNext',
                moduleResolution: 'bundler',
                resolveJsonModule: true,
                strict: true,
                skipLibCheck: true,
                outDir: 'dist'
            },
            include: ['**/*.ts'],
            exclude: ['node_modules', 'dist']
        }, null, 2);
    },

    _agentsMd(modelName, topMeasures) {
        const measureList = topMeasures
            .map(m => `- \`[${m.name}]\` (${m.table}${m.visualCount ? `, used in ${m.visualCount} visuals` : ''})`)
            .join('\n');

        return [
            `# ${modelName} — Agent Context`,
            ``,
            `This is a Rayfin Fabric Data App generated from the **${modelName}** semantic model.`,
            ``,
            `## Model context`,
            ``,
            `Full model documentation (tables, columns, DAX expressions, relationships):`,
            `→ **model-ctx.md** in the parent dashboard folder`,
            ``,
            `## Top measures (by visual coverage)`,
            ``,
            measureList,
            ``,
            `## Visual files`,
            ``,
            `Each visual has three files with the same base name in \`visuals/\`:`,
            `| File | Purpose |`,
            `|---|---|`,
            `| \`.dax\` | DAX query with \`<YEAR>\` and \`<FILTER>\` placeholders |`,
            `| \`.json\` | D3.js visual spec (chart type, encoding, colors) |`,
            `| \`.ts\` | TypeScript wiring: query() maps DAX result to spec data |`,
            ``,
            `## Copilot rules`,
            ``,
            `- **Never invent field names** — all table and measure names come from model-ctx.md`,
            `- DAX references: measures as \`[MeasureName]\`, columns as \`TableName[ColumnName]\``,
            `- When adding a new visual: create all three files (.dax, .json, .ts) and add to index.ts`,
            `- D3 drilldown: use \`d3.hierarchy()\`, separate .dax per level with \`<LEVEL>\` placeholder`,
            `- D3 matrix/pivot: use \`d3.rollup()\` for pivot, manage expanded-row state in .ts`,
            `- For DAX queries with time context: replace \`<YEAR>\` with the selected year filter`,
            `- For cross-visual filters: replace \`<FILTER>\` with a KEEPFILTERS/CALCULATETABLE expression`,
            ``,
            `## Deployment`,
            ``,
            `\`\`\`bash`,
            `bun install          # install dependencies`,
            `bun run dev          # local preview at localhost:5173 (queries live semantic model)`,
            `bunx rayfin up       # deploy to Fabric`,
            `\`\`\``,
            ``
        ].join('\n');
    },

    _readmeMd(modelName, topMeasures) {
        const measureRows = topMeasures
            .map(m => `| ${m.name} | ${m.table} | ${m.visualCount || '—'} |`)
            .join('\n');

        return [
            `# ${modelName} — Fabric Data App`,
            ``,
            `Auto-generated Rayfin project scaffold from [PBIP SemLin](https://bredeespelid.github.io/PBIP_SemLin/).`,
            ``,
            `## Quick start`,
            ``,
            `\`\`\`bash`,
            `# 1. Update fabric.yaml with your workspace name and dataset ID`,
            `# 2. Install and run`,
            `bun install`,
            `bun run dev`,
            `\`\`\``,
            ``,
            `Open [localhost:5173](http://localhost:5173) — live queries against your semantic model.`,
            ``,
            `## Top measures`,
            ``,
            `| Measure | Table | Visuals |`,
            `|---|---|---|`,
            measureRows,
            ``,
            `## File structure`,
            ``,
            `\`\`\``,
            `fabric.yaml          ← workspace + dataset connection`,
            `AGENTS.md            ← Copilot context`,
            `index.ts             ← barrel export of all visuals`,
            `visuals/`,
            topMeasures.map(m => {
                const s = this._safeName(m.name);
                return `  ${s}.dax\n  ${s}.json\n  ${s}.ts`;
            }).join('\n'),
            `\`\`\``,
            ``,
            `## D3 features`,
            ``,
            `**Drilldown** — use \`d3.hierarchy()\`, one .dax file per drill level with \`<LEVEL>\` placeholder.`,
            ``,
            `**Matrix / pivot** — use \`d3.rollup()\` for aggregation, manage expanded row state in .ts.`,
            ``,
            `Ask Copilot: *"Add drilldown for [${topMeasures[0]?.name || 'MeasureName'}] by quarter and month"*`,
            `or *"Show [${topMeasures[0]?.name || 'MeasureName'}] as a matrix with rows = category, columns = year"*`,
            ``
        ].join('\n');
    },

    _daxQuery(measure) {
        // Try to find a useful dimension column from the table — use measure name as hint
        const dimHint = this._guessDimension(measure);
        return [
            `-- ${measure.name}`,
            `-- Auto-generated DAX query — update <YEAR> and <FILTER> before deploying`,
            `-- Table: ${measure.table}`,
            `--`,
            `-- Placeholders:`,
            `--   <YEAR>   → replace with a year value or remove the filter for all time`,
            `--   <FILTER> → replace with a KEEPFILTERS expression for cross-visual filtering`,
            ``,
            `EVALUATE`,
            `SUMMARIZECOLUMNS(`,
            dimHint ? `    ${dimHint},` : `    -- Add dimension columns here, e.g. 'DimDate'[Year]`,
            `    <FILTER>,`,
            `    "Value", [${measure.name}]`,
            `)`,
            dimHint ? `ORDER BY ${dimHint}` : ``,
            ``
        ].filter((l, i, a) => !(l === '' && a[i - 1] === '')).join('\n');
    },

    _d3Spec(measure) {
        const spec = {
            type: 'bar',
            title: measure.name,
            measure: `[${measure.name}]`,
            table: measure.table,
            encoding: {
                x: {
                    field: 'Value',
                    type: 'quantitative',
                    title: measure.name,
                    format: ',.0f'
                },
                y: {
                    field: 'Category',
                    type: 'nominal',
                    title: '',
                    sort: '-x'
                },
                color: {
                    value: '#7ec8e3'
                },
                tooltip: [
                    { field: 'Category', type: 'nominal' },
                    { field: 'Value',    type: 'quantitative', title: measure.name, format: ',.0f' }
                ]
            },
            theme: {
                background: 'transparent',
                font: 'Segoe UI, system-ui, sans-serif',
                labelColor: '#aaa',
                axisColor: 'rgba(126,200,227,0.15)'
            },
            drilldown: {
                enabled: false,
                levels: ['Year', 'Quarter', 'Month'],
                placeholder: '<LEVEL>'
            }
        };
        return JSON.stringify(spec, null, 2);
    },

    _typescript(measure, modelName) {
        const safe   = this._safeName(measure.name);
        const pascal = this._pascal(measure.name);
        return [
            `// ${measure.name} — auto-generated Rayfin visual`,
            `// Table: ${measure.table} | Model: ${modelName}`,
            `// Update query() with the DAX from ${safe}.dax, then run: bun run dev`,
            ``,
            `import spec from './${safe}.json';`,
            ``,
            `// Types are declared by @microsoft/rayfin — install with: bun install`,
            `// eslint-disable-next-line @typescript-eslint/no-explicit-any`,
            `type RayfinClient = any;`,
            ``,
            `interface QueryContext {`,
            `    year?:    number;`,
            `    filters?: Record<string, string[]>;`,
            `}`,
            ``,
            `const ${pascal}Visual = {`,
            `    spec,`,
            ``,
            `    /**`,
            `     * Execute the DAX query and return rows for the D3 spec.`,
            `     * Replace the inline DAX string with the content of ${safe}.dax`,
            `     * once you have confirmed the query returns the expected columns.`,
            `     */`,
            `    async query(client: RayfinClient, context: QueryContext = {}) {`,
            `        const yearFilter = context.year`,
            `            ? \`FILTER(ALL('DimDate'[Year]), 'DimDate'[Year] = \${context.year})\``,
            `            : 'ALL()';`,
            ``,
            `        const dax = \``,
            `EVALUATE`,
            `SUMMARIZECOLUMNS(`,
            `    KEEPFILTERS(\${yearFilter}),`,
            `    "Value", [${measure.name}]`,
            `)\`.trim();`,
            ``,
            `        const result = await client.executeQueries([{ query: dax }]);`,
            `        const rows   = result?.results?.[0]?.tables?.[0]?.rows ?? [];`,
            ``,
            `        // Map DAX column names (e.g. "[Value]") to spec field names ("Value")`,
            `        return rows.map((row: Record<string, unknown>) => ({`,
            `            Category: row['[Category]'] ?? row[Object.keys(row).find(k => k !== '[Value]') ?? ''] ?? '',`,
            `            Value:    Number(row['[Value]'] ?? 0)`,
            `        }));`,
            `    }`,
            `};`,
            ``,
            `export default ${pascal}Visual;`,
            ``
        ].join('\n');
    },

    _barrelExport(safeNames) {
        const lines = [
            `// Auto-generated barrel export — add new visuals here`,
            ``
        ];
        for (const name of safeNames) {
            const pascal = this._pascal(name.replace(/-/g, '_'));
            lines.push(`export { default as ${pascal}Visual } from './visuals/${name}';`);
        }
        lines.push('');
        return lines.join('\n');
    },

    // ─────────────────────────────────────────────────────────
    // Helpers
    // ─────────────────────────────────────────────────────────

    _safeName(name) {
        return name
            .replace(/[^a-zA-Z0-9\s_-]/g, '')
            .trim()
            .replace(/\s+/g, '_');
    },

    _kebab(name) {
        return name
            .replace(/([a-z])([A-Z])/g, '$1-$2')
            .replace(/[\s_]+/g, '-')
            .toLowerCase()
            .replace(/[^a-z0-9-]/g, '');
    },

    _pascal(safeName) {
        return safeName
            .split(/[_\s-]+/)
            .map(w => w.charAt(0).toUpperCase() + w.slice(1))
            .join('');
    },

    _guessDimension(measure) {
        // Common dimension patterns — the agent will update these
        const name = measure.name.toLowerCase();
        if (name.includes('sales') || name.includes('revenue') || name.includes('amount')) {
            return `'DimDate'[Year]`;
        }
        if (name.includes('count') || name.includes('units') || name.includes('qty')) {
            return `'DimDate'[Month]`;
        }
        return null;
    }
};
